// code has been taken from http://docs.opencv.org/2.4/modules/contrib/doc/facerec/facerec_tutorial.html
// some modifications have been done to suit our requirements.
#include "opencv2/core/core.hpp"
#include "opencv2/contrib/contrib.hpp"
#include "opencv2/highgui/highgui.hpp"

#include <iostream>
#include <fstream>
#include <sstream>

using namespace cv;
using namespace std;


// reads a csv file and prepares the arrays of training data.
static void read_csv(const string& filename, vector<Mat>& images, vector<int>& labels, char separator = ';') {
    std::ifstream file(filename.c_str(), ifstream::in);
    if (!file) {
        string error_message = "No valid input file was given, please check the given filename.";
        CV_Error(CV_StsBadArg, error_message);
    }
    string line, path, classlabel;
    while (getline(file, line)) {
        stringstream liness(line);
        getline(liness, path, separator);
        getline(liness, classlabel);
        if(!path.empty() && !classlabel.empty()) {
            Mat image=Mat::zeros(100,100,CV_32F);
            Mat img=imread(path,0);
            resize(img,image,image.size(),0,0);
            images.push_back(image);
            labels.push_back(atoi(classlabel.c_str()));
        }
    }
}

int main() {


    // Get the path to your CSV.
    string fn_csv = string("C:/Users/MRIDUL/Desktop/fossee/task1/faces.csv");

    // These vectors hold the images and corresponding labels.
    vector<Mat> images;
    vector<int> labels;
    // Read in the data. This can fail if no valid
    // input filename is given.
    try {
        read_csv(fn_csv, images, labels);
    } catch (cv::Exception& e) {
        cerr << "Error opening file \"" << fn_csv << "\". Reason: " << e.msg << endl;
        // nothing more we can do
        exit(1);
    }

    // Quit if there are not enough images for this demo.
    if(images.size() <= 1) {
        string error_message = "This demo needs at least 2 images to work. Please add more images to your data set!";
        CV_Error(CV_StsError, error_message);
    }


    Ptr<FaceRecognizer> model = createFisherFaceRecognizer();
    model->train(images, labels);
    // change the name of the xml file.
    model->save("faces_trained.xml");



    //cout<<predictedLabel;
    return 0;
}
